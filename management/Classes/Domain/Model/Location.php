class Location extends \TYPO3\CMS\Extbase\DomainObject\AbstractEntity {

	/**
	 * address
	 *
	 * @var \string
	 * @validate NotEmpty
	 */
	protected $address;

	/**
	 * zip
	 *
	 * @var \string
	 * @validate NotEmpty
	 */
	protected $zip;

	/**
	 * city
	 *
	 * @var \string
	 * @validate NotEmpty
	 */
	protected $city;

	/**
	 * Returns the address
	 *
	 * @return \string $address
	 */
	public function getAddress() {
		return $this->address;
	}

	/**
	 * Sets the address
	 *
	 * @param \string $address
	 * @return void
	 */
	public function setAddress($address) {
		$this->address = $address;
	}

	/**
	 * Returns the zip
	 *
	 * @return \string $zip
	 */
	public function getZip() {
		return $this->zip;
	}

	/**
	 * Sets the zip
	 *
	 * @param \string $zip
	 * @return void
	 */
	public function setZip($zip) {
		$this->zip = $zip;
	}

	/**
	 * Returns the city
	 *
	 * @return \string $city
	 */
	public function getCity() {
		return $this->city;
	}

	/**
	 * Sets the city
	 *
	 * @param \string $city
	 * @return void
	 */
	public function setCity($city) {
		$this->city = $city;
	}
}
